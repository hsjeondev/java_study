package com.gn.practice;

public class Practice01 {

	public static void main(String[] args) {
		
		System.out.println("기존 코드");
		System.out.println("100 + 10 = " + (100 + 10));
		System.out.println("10 * (100 + 10) = " + (10 * (100 + 10)));
		System.out.println("10 * (100 + 10) / 10 = " + (10 * (100 + 10) / 10));
		System.out.println();
		
		System.out.println("기존 코드");
		int x = 10;
		int y = 100;
		
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		System.out.println("y + x = " + (y + x));
		System.out.println("x * (y + x) = " + (x * (y + x)));
		System.out.println("x * (y + x) / x = " + (x * (y + x) / x));

	}

}
